package m16_loops_part1;

public class Numbers {

    public static void main(String[] args) {

        //print numbers from 0 - 10 (inclusive) in the same line

        for(int i = 0; i < 11; i++){
            System.out.print(i + " ");
            //-> only print so we have nums all in one line and i + " " to leave space in between

        }
        System.out.println(" "); //to start new line
        System.out.println("Hello");//printed below 1-10



    }
}
